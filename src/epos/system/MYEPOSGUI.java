       public void checkTheLogin(){
        
  try
        {
     
        //programatically create the labels and textfields for the optionpane
        JLabel label_login = new JLabel("Employee number:");
        JTextField login = new JTextField();
        JLabel label_password = new JLabel("Password:");
        JPasswordField password = new JPasswordField();
        //-----------------------------------------------------
        // store these in an object array
        Object[] login_GUI_Elelemnts = { label_login, login, label_password, password };
        //-----------------------------------------------------
        int userInteraction = JOptionPane.showConfirmDialog(null, login_GUI_Elelemnts, "Login",
        JOptionPane.OK_CANCEL_OPTION,
        JOptionPane.PLAIN_MESSAGE);
        // this integer variable 'userInteraction' will hold the result of
        //the users interaction with the JOpttionPane
        //we will then test the result against common returned values. OK OPtion , Cancel , Closed
        //-----------------------------------------------------
       
            if (userInteraction == JOptionPane.OK_OPTION)
            {
                    String user=login.getText().trim(); // user string that the user entered in the textfield
                    String pass=new String(password.getPassword()); //pulls the password string from textfield
                    Boolean entryGranted = false ;// setup boolean as false.


                    theUser=new User();//Instantiate this user object
                    theUser.setEmployee_Number(Integer.parseInt(user));// set the PIN Number 
                    theUser.setPassword(pass);//and Username fields of the object using the setters

                    System.out.println("The users data is now: " + theUser.toString());

                    Users_DATA_Handler myUsersHandler= new Users_DATA_Handler();//Instantiate a Users Data handler


                        if (myUsersHandler.checkUserIsValid(theUser)){//ou have a valid user you can then use the method getValidUser of the DB Handler

                            theUser = myUsersHandler.getValidUser(theUser);
                            System.out.println("The users data is now: " + theUser.toString());

                            //to update the employe number
                            employee_Number=theUser.getEmployee_Number();

                        }
                        else
                        {
                            JOptionPane.showMessageDialog(this, "You have not supplied valid credentials\n Please re-enter your valid Employee number and Password");
                            checkTheLogin();
                        }



                //if (( user.toUpperCase().equals ("U")) && pass.toUpperCase().equals ("2")) // this line will change 
                if (theUser.getRole().toUpperCase().equals("USER"))
                    {
                        JOptionPane.showMessageDialog(this, "Logged in as user\nAdmin panel will be removed");
                        //If an ordinary user logged in then remove the panel tabs that relate to administrators.
                        //removes the admin tabs
                        this.tabMain.remove(tabProductCRUD);
                        this.tabMain.remove(tabUserCRUD);
                        this.tabMain.remove(pnlResetSystem);
                        entryGranted=true;

                        //display current user details in the agent tab
                        lbl_EmployeeNumber.setText("" + theUser.getEmployee_Number());
                        lbl_FirstName.setText("" + theUser.getFirst_Name());
                        lbl_LastName.setText("" + theUser.getLast_Name());
                        //lbl_Password.setText("" + theUser.getPassword());
                        lbl_Password.setText("*******");
                        lbl_Role.setText("" + theUser.getRole());

                    }
                // if (( user.toUpperCase().equals ("M")) && pass.toUpperCase().equals ("1")) // this line will change
                if (theUser.getRole().toUpperCase().equals("MANAGER"))
                    {
                        JOptionPane.showMessageDialog(this, "Logged in as manager\nAccess granted to all areas");
                        entryGranted=true;

                        //display current user details in the agent tab
                        lbl_EmployeeNumber.setText("" + theUser.getEmployee_Number());
                        lbl_FirstName.setText("" + theUser.getFirst_Name());
                        lbl_LastName.setText("" + theUser.getLast_Name());
                        //lbl_Password.setText("" + theUser.getPassword());
                        lbl_Password.setText("*******");
                        lbl_Role.setText("" + theUser.getRole());
                    }
                else if (!entryGranted) //entry denied
                    {
                        JOptionPane.showMessageDialog(this, "Go chase yourself - entry denied");
                        userInteraction = JOptionPane.CANCEL_OPTION;
                    }
            }
            else
            {
                //user clicked on CANCEL or the X close button- we change the userInteraction to force execution of the CANCEL block of code
                userInteraction = JOptionPane.CANCEL_OPTION;
            }
            if ( (userInteraction == JOptionPane.CANCEL_OPTION) || (userInteraction == JOptionPane.CLOSED_OPTION) )
                // the cancel block of code - if user presses the X or CANCEL
            {
                //feedback to user and close the application as no suitable login credentials have been supplied.
                JOptionPane.showMessageDialog(this, "You have not supplied valid credentials\n the application will now terminate");
                System.exit(0);
            }
        }
        catch(Exception ex)
        {
            System.out.println("getValidUser : Error");
            System.out.println("SQL exception occured" + ex);
            JOptionPane.showMessageDialog(this, "You have not supplied valid credentials\n Please re-enter your valid Employee number and Password");
            checkTheLogin();
        }
       
    }             
   
    
