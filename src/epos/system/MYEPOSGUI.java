    private void btn_DeleteProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteProductActionPerformed

        //check that the user really wants to delete the data before continuing
        int userChoice = JOptionPane.showConfirmDialog (null,
            "Are you sure you wish to delete this product data?",
            "Delete System Request", JOptionPane.YES_NO_OPTION);

        if(userChoice == JOptionPane.YES_OPTION)
        {
            if (comboUpdateProduct.getSelectedItem() != null) {
                System.out.println(comboUpdateProduct.getSelectedItem().toString());
                String theUserSelectedItem = comboUpdateProduct.getSelectedItem().toString();
                //to find the space between the id number and the product name
                int spacePosition = theUserSelectedItem.indexOf(" ");
                int ID = Integer.parseInt(theUserSelectedItem.substring(0,spacePosition));

                try {
                    Connection con=DB_Utils.getConnection();
                    Statement stmt = con.createStatement();
                    stmt.executeUpdate(" DELETE * from Products where ID=" + ID);
                }
                catch (Exception ex)
                {
                    String message = ex.getMessage();
                    System.out.println(message);
                }
                comboDleteProduct.removeAllItems();
            }
            //to refresh comboboxx
            JOptionPane.showMessageDialog(null,"Successfully Deleted!");
        }

    }//GEN-LAST:event_btn_DeleteProductActionPerformed

